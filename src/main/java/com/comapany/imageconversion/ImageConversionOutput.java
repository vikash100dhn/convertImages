package com.comapany.imageconversion;

public class ImageConversionOutput {
	
	private String fileName;
	private String storeFileName;
	private String onlineFileLocation;
	private char barCodeFlag;
	private String imageIconData;
	private String barCode;
	private String barCodeType;
	
	
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getStoreFileName() {
		return storeFileName;
	}
	public void setStoreFileName(String storeFileName) {
		this.storeFileName = storeFileName;
	}
	public String getOnlineFileLocation() {
		return onlineFileLocation;
	}
	public void setOnlineFileLocation(String onlineFileLocation) {
		this.onlineFileLocation = onlineFileLocation;
	}
	public char getBarCodeFlag() {
		return barCodeFlag;
	}
	public void setBarCodeFlag(char barCodeFlag) {
		this.barCodeFlag = barCodeFlag;
	}
	public String getImageIconData() {
		return imageIconData;
	}
	public void setImageIconData(String imageIconData) {
		this.imageIconData = imageIconData;
	}
	public String getBarCode() {
		return barCode;
	}
	public void setBarCode(String barCode) {
		this.barCode = barCode;
	}
	public String getBarCodeType() {
		return barCodeType;
	}
	public void setBarCodeType(String barCodeType) {
		this.barCodeType = barCodeType;
	}
}
